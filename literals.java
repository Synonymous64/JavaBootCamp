
//! A constant value which can be assigned to the variable is called as a literal
public class literals {
    public static void main(String[] args) {
        byte a = 90;
        char str = 'A';
        float f = 23.4f; //! need to put f behind the digit to typecast it to float, default is double
        double d = 23.8;
        long l = 2349999999l;
        String s = "woej";
        System.out.println(a +" "+ str + " " + f + " " + d + " " + l + " "+ s);
    }
}
